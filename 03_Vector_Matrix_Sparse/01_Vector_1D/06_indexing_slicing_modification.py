## vector[index] = new_value ##
###############################

vector_demo = vector.copy()  # Create a copy to avoid modifying the original vector

vector_demo[0] = 100.0
print(f"Modified: {vector_demo[0]}\nOriginal: {vector[0]}")
# Modified: 100.0
# Original: 49.0

vector_demo[10:15] = [200.0, 201.0, 202.0, 203.0, 204.0]
print(f"Modified slice: {vector_demo[10:15]}\nOriginal slice: {vector[10:15]}")
# Modified slice: [200. 201. 202. 203. 204.]
# Original slice: [61. 66. 70. 72. 74.]

vector_demo[2:5] = 0.5
print(f"Modified slice: {vector_demo[2:5]}\nOriginal slice: {vector[2:5]}")
# Modified slice: [0.5 0.5 0.5]
# Original slice: [49. 49. 49.]

vector_demo[[20, 25, 30]] = [300.0, 301.0, 302.0]
print(f"Modified indices: {vector_demo[[20, 25, 30]]}\nOriginal indices: {vector[[20, 25, 30]]}")
# Modified indices: [300. 301. 302.]
# Original indices: [62. 57. 49.]

vector_demo[[-1, -2, -3]] = 25.5
